print('>>> Tipo Primitivos <<<')

algo = input('Digite algo: ')
print('O tipo prmitivo de algo é ', type(algo))

print('Só tem espaço? ', algo.isspace())
print('Só letra maiúscula? ', algo.isupper())
print('É alfanumérica?', algo.isalnum())
print('É numérico?', algo.isnumeric())
print('Está capitalizada?', algo.istitle())
print('Só tem letras minúsculas?', algo.islower())






